#Objetivo: Crear un programa que pueda realizar una secuencia de números utilizando FOR y WHILE.
#Entrada: Mostrar secuencia de números
"""
Procesos principales
1. Realizar las secuencias de números requeridas
2. Mostrar el resultado
"""
print("Programa para realizar secuencias de números")
#Salida
print("Ejercicio Semana 8: Secuencia de números utilizando FOR y WHILE")


for i in range(1, 26):
    print(i )
i = 1

while i <= 25:
    print(i )
    i += 1
for i in range(5, 51, 5):
    print(i )
i = 5
while i <= 50:
    print(i )
    i += 5
for i in range(20, -2, -2):
    print(i )
i = 20
while i >= 0:
    print(i )
    i -= 2
